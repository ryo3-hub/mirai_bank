/// issue #122 フォロー: 連続記録日数に応じた励ましメッセージのレパートリー。
///
/// 連続日数で 7 段階に分け（0日 = tier0 を含む）、それぞれ約 30 個のメッセージを
/// 用意。アプリ起動ごとに `pickRandomEncouragement(days)` で **ランダム** に
/// 1 つ選ぶ（issue: アプリ起動ごとに変わるひとこと）。
library;

import 'dart:math';

/// 連続日数に応じたティアから **ランダム** に 1 つメッセージを選ぶ。
///
/// - `days` が 0 でも tier0 から励ましメッセージを返す
///   （作業前のモチベ用途。issue: 作業前にやる気が欲しい）
/// - `random` を渡せばテスト時の決定論性を確保できる
String pickRandomEncouragement(int days, {Random? random}) {
  final pool = _poolFor(days);
  final r = random ?? Random();
  return pool[r.nextInt(pool.length)];
}

/// 当日の日付（タイムゾーン考慮済み）から決定論的に 1 つメッセージを選ぶ。
///
/// 旧 API。新規利用は `pickRandomEncouragement` を推奨。
/// 既存テストとの互換のため残しているが、UI からの呼び出しは無くなった。
String pickStreakMessage(int days, {DateTime? now}) {
  if (days <= 0) return '';
  final pool = _poolFor(days);
  final today = now ?? DateTime.now();
  // ローカル日付ベースの index。タイムゾーンを跨いだ深夜運用でも安定。
  final epochDay = DateTime(today.year, today.month, today.day)
          .millisecondsSinceEpoch ~/
      Duration.millisecondsPerDay;
  final index = epochDay.remainder(pool.length).abs();
  return pool[index];
}

List<String> _poolFor(int days) {
  if (days >= 365) return _tier6;
  if (days >= 100) return _tier5;
  if (days >= 30) return _tier4;
  if (days >= 7) return _tier3;
  if (days >= 3) return _tier2;
  if (days >= 1) return _tier1;
  return _tier0;
}

